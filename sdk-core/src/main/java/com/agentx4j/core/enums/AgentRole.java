package com.agentx4j.core.enums;

/**
 * Agent 角色枚举。
 *
 * <p>定义 Agent 在经济体中的角色定位。</p>
 */
public enum AgentRole {

    /**
     * 买家 — 只消费服务，不提供服务。
     * <p>类比：纯消费者，只花钱不赚钱。</p>
     */
    BUYER,

    /**
     * 卖家 — 只提供服务，不消费服务。
     * <p>类比：纯商家，只赚钱不花钱（在平台上）。</p>
     */
    SELLER,

    /**
     * 两者兼备 — 既提供服务也消费服务。
     * <p>类比：既开餐厅也去其他餐厅吃饭。</p>
     * <p>这是最常见的情况，因为 Agent 通常需要调用其他 Agent 的能力来完成复杂任务。</p>
     */
    BOTH
}
